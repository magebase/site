export const getUseCaseBySlug = (slug: string): UseCaseData | undefined => {
  return useCaseData.find(useCase => useCase.slug === slug);
};

export const getUseCasesByCategory = (category: string): UseCaseData[] => {
  return useCaseData.filter(useCase => useCase.category === category);
};

export const getAllCategories = (): string[] => {
  return Array.from(new Set(useCaseData.map(useCase => useCase.category)));
};
